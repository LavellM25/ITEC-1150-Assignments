import math

# cut off 7.89 to 7
print(math.floor(7.89))

# round 54.345395 to 54.345
rounded_number = round(54.345395, 3)
print(rounded_number)

# calculate the square root of 2
print(math.sqrt(2))

# calculate the sin of 7
print(f'{math.sin(7)}')

# display the value of pi
print(math.pi)

# display pi rounded to 3 decimal places
rounded_pi = round(math.pi, 3)
print(rounded_pi)
